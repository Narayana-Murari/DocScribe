const $ = (id) => document.getElementById(id);
let patients = [];

async function init() {
  const [pats, provs] = await Promise.all([
    fetch('/api/patients').then((r) => r.json()),
    fetch('/api/providers').then((r) => r.json()),
  ]);
  patients = pats;

  $('patient').innerHTML = pats
    .map((p) => `<option value="${p.id}">${p.name} · ${p.patientUid}</option>`)
    .join('');
  $('provider').innerHTML = provs
    .map((p) => `<option value="${p.id}" ${p.configured ? '' : 'disabled'}>${p.label}${p.configured ? '' : ' (not configured)'}</option>`)
    .join('');

  $('patient').addEventListener('change', renderPatient);
  $('transcript').addEventListener('input', () => {
    $('scribeBtn').disabled = !$('transcript').value.trim();
  });
  $('simulateBtn').addEventListener('click', simulate);
  $('scribeBtn').addEventListener('click', scribe);
  $('clearBtn').addEventListener('click', clearAll);
  renderPatient();
}

function currentPatient() {
  return patients.find((p) => p.id === $('patient').value);
}

function renderPatient() {
  const p = currentPatient();
  $('patientCard').innerHTML = `
    <div class="name">${p.name}</div>
    <div class="muted">${p.age}y · ${p.gender} · ${p.bloodGroup} · ${p.village}</div>
    ${p.allergies.length ? `<div class="allergy">⚠ Allergies: ${p.allergies.join(', ')}</div>` : ''}
    <div class="section"><b>Conditions</b><br>${p.conditions.join('<br>')}</div>
    <div class="section"><b>Current meds</b><br>${p.currentMedications.join('<br>')}</div>
    <div class="section"><b>Last vitals</b><br>BP ${p.vitalsHistory.bp} · Pulse ${p.vitalsHistory.pulse} · SpO₂ ${p.vitalsHistory.spo2}</div>`;
  clearAll();
}

function clearAll() {
  $('transcript').value = '';
  $('transcriptSource').textContent = '';
  $('activity').innerHTML = '';
  $('note').classList.add('hidden');
  $('note').innerHTML = '';
  $('agentStatus').textContent = '';
  $('scribeBtn').disabled = true;
}

async function simulate() {
  const btn = $('simulateBtn');
  btn.disabled = true;
  btn.textContent = '… Simulating visit';
  $('transcriptSource').textContent = '';
  try {
    const res = await fetch('/api/simulate', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ patientId: $('patient').value, provider: $('provider').value }),
    });
    const data = await res.json();
    $('transcript').value = data.transcript || '';
    $('transcriptSource').textContent =
      data.source === 'canned' ? 'pre-recorded (fallback)' : `generated by ${data.source} · ${(data.ms / 1000).toFixed(1)}s`;
    $('scribeBtn').disabled = !$('transcript').value.trim();
  } catch (e) {
    alert('Simulation failed: ' + e.message);
  } finally {
    btn.disabled = false;
    btn.textContent = '▶ Simulate Visit';
  }
}

const TOOL_LABELS = {
  get_patient_record: '🗂 Retrieving patient record',
  check_drug_interactions: '⚗️ Checking drug interactions',
  suggest_icd_codes: '🏷 Suggesting ICD-10 codes',
  flag_followup: '🚩 Raising review flag',
  save_soap_note: '💾 Saving SOAP note',
};

function addEvt(cls, html) {
  const div = document.createElement('div');
  div.className = `evt ${cls}`;
  div.innerHTML = html;
  $('activity').appendChild(div);
  $('activity').scrollTop = $('activity').scrollHeight;
}

async function scribe() {
  const btn = $('scribeBtn');
  btn.disabled = true;
  $('activity').innerHTML = '';
  $('note').classList.add('hidden');
  $('agentStatus').textContent = 'working…';

  try {
    const res = await fetch('/api/scribe', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({
        patientId: $('patient').value,
        transcript: $('transcript').value,
        provider: $('provider').value,
      }),
    });
    const reader = res.body.getReader();
    const decoder = new TextDecoder();
    let buf = '';
    while (true) {
      const { done, value } = await reader.read();
      if (done) break;
      buf += decoder.decode(value, { stream: true });
      const parts = buf.split('\n\n');
      buf = parts.pop();
      for (const part of parts) {
        const line = part.trim();
        if (line.startsWith('data: ')) handleEvent(JSON.parse(line.slice(6)));
      }
    }
  } catch (e) {
    addEvt('err', 'Stream failed: ' + e.message);
  } finally {
    btn.disabled = false;
    if ($('agentStatus').textContent === 'working…') $('agentStatus').textContent = '';
  }
}

function handleEvent(ev) {
  switch (ev.type) {
    case 'start':
      addEvt('ok', `Agent started on ${ev.provider}`);
      break;
    case 'tool_start': {
      const label = TOOL_LABELS[ev.name] || ev.name;
      const detail = ev.name === 'check_drug_interactions' ? (ev.input.medications || []).join(', ')
        : ev.name === 'suggest_icd_codes' ? (ev.input.diagnoses || []).join(', ')
        : ev.name === 'flag_followup' ? `${ev.input.severity}: ${ev.input.reason}`
        : '';
      addEvt(ev.name === 'flag_followup' ? 'flag' : 'tool', `${label}${detail ? `<small>${esc(detail)}</small>` : ''}`);
      break;
    }
    case 'tool_result':
      if (ev.name === 'check_drug_interactions' && ev.output.interactions?.length) {
        for (const i of ev.output.interactions) {
          addEvt('flag', `⚠ ${i.severity}: ${esc(i.pair.join(' + '))}<small>${esc(i.note)}</small>`);
        }
      }
      break;
    case 'text':
      addEvt('text', esc(ev.text));
      break;
    case 'note':
      renderNote(ev.note);
      break;
    case 'done':
      addEvt('ok', `✓ Done in ${(ev.ms / 1000).toFixed(1)}s`);
      $('agentStatus').textContent = `done · ${(ev.ms / 1000).toFixed(1)}s`;
      break;
    case 'error':
      addEvt('err', '✗ ' + esc(ev.message));
      $('agentStatus').textContent = 'error';
      $('agentStatus').className = 'pill warn';
      break;
  }
}

function renderNote(note) {
  const el = $('note');
  el.classList.remove('hidden');
  el.innerHTML = `
    <h3>Draft SOAP Note — ${esc(currentPatient().name)}</h3>
    <div class="soap">
      <b>Subjective</b>${esc(note.subjective || '')}
      <b>Objective</b>${esc(note.objective || '')}
      <b>Assessment</b>${esc(note.assessment || '')}
      <b>Plan</b>${esc(note.plan || '')}
    </div>
    ${note.icdCodes?.length ? `<div class="codes">${note.icdCodes.map((c) => `<span class="code">${esc(c)}</span>`).join('')}</div>` : ''}
    ${note.medications?.length ? `<div class="soap"><b>Medications</b>${note.medications.map(esc).join('<br>')}</div>` : ''}
    ${note.flags?.length ? note.flags.map((f) => `<div class="flagline">🚩 ${esc(f)}</div>`).join('') : ''}
    <button class="btn primary approve" onclick="this.textContent='✓ Approved & signed'; this.disabled=true;">Approve &amp; Sign</button>`;
}

function esc(s) {
  return String(s).replace(/[&<>"]/g, (c) => ({ '&': '&amp;', '<': '&lt;', '>': '&gt;', '"': '&quot;' }[c]));
}

init();
